    boolean agregarRuta(String ruta1, String ruta2, String hora1, String hora2, String date){
        
        try{
            sql = "INSERT INTO `tb_ruta`(`ciclista_fecha`, `ciclista_horaInicio`, `ciclista_horaFin`, `ciclista_lugarInicio`, `ciclista_lugarFin`, `ciclista_id`) VALUES (?,?,?,?,?,?)";
            ps = con.getConnection().prepareStatement(sql);
            ps.setString(1, date);
            ps.setString(2, hora1);
            ps.setString(3, hora2);
            ps.setString(4, ruta1);
            ps.setString(5, ruta2);
            ps.setInt(6, idLog);
            ps.executeUpdate();
            return true;
        }catch(SQLException ex){
            System.err.println("Error de conexi贸n:" + ex.getMessage());
            return false;
        }
    }
    boolean EditarRuta(String ruta1, String ruta2, String hora1, String hora2, String date, int id){
        
        try{
            sql = "UPDATE  tb_ruta SET ciclista_lugarInicio = ?, ciclista_lugarFin  = ?, ciclista_fecha = ?, ciclista_horaInicio=?, ciclista_horaFin=?  WHERE ruta_id=?";
            ps = con.getConnection().prepareStatement(sql);
            ps.setString(1, ruta1);
            ps.setString(2, ruta2);
            ps.setString(3, hora1);
            ps.setString(4, hora2);
            ps.setString(5, date);
            ps.setInt(6, id);
            ps.executeUpdate();
            return true;
        }catch(SQLException ex){
            System.err.println("Error de conexi贸n:" + ex.getMessage());
            return false;
        }
    }
    
    
    void show_rutas(){
        String query = "SELECT ciclista_lugarInicio, ciclista_lugarFin, ciclista_fecha, ruta_id FROM tb_ruta WHERE ciclista_id=?";
        try {
            cn = con.getConnection();
            ps = cn.prepareStatement(query);
            ps.setInt(1,idLog);
            rs = ps.executeQuery();
            Object[] ruta = new Object[4];
            model = (DefaultTableModel)tbl_ruta.getModel();
            while (rs.next()) {
                ruta[0] = rs.getString("ciclista_lugarInicio");
                ruta[1] = rs.getString("ciclista_lugarFin");
                ruta[2] = rs.getString("ciclista_fecha");
                ruta[3] = rs.getString("ruta_id");
                model.addRow(ruta);
            }
            tbl_ruta.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "No se encontr贸 ruta ");
        }
    }
    
    void eliminarRuta(int id){
        try{
            String sql = "DELETE FROM tb_ruta WHERE ruta_id=?";
            cn = con.getConnection();
            ps = cn.prepareStatement(sql);
            ps.setInt(1,id);
            ps.executeUpdate();
            clear_rows_table();
            JOptionPane.showMessageDialog(this, "Se ha eliminado el registro");
            show_rutas();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "No se pudo eliminar la informaci贸n del registro " + e );
        }
    }
    void clear_rows_table(){
        for (int i = 0; i <tbl_ruta.getRowCount(); i++) {
            model.removeRow(i);
            i =-1;
        }
        txt_lugarInicio.setText("");
        txt_lugarFin.setText("");
        txt_fecha.setText("");
        txt_horaInicio.setText("");
        txt_horaFin.setText("");
    }


